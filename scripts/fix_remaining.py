def fix_writing_file(filepath, name):
    with open(filepath, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    fixed = 0
    not_found = 0
    for item in data:
        char = item.get('char', '')
        if char in CHAR_DB:
            sc, rad = CHAR_DB[char]
            item['stroke_count'] = sc
            item['radical'] = rad
            item['strokes'] = [f"Stroke order: {sc} strokes, radical: {rad}. Visit strokeorder.com for animation."]
            fixed += 1
        else:
            not_found += 1
            item['stroke_count'] = max(1, len(char) * 3)  # rough estimate
            item['radical'] = char[0] if char else '?'
            item['strokes'] = ["Visit strokeorder.com for animated stroke order."]
    
    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    print(f"  {name}: {fixed} fixed, {not_found} fallback (total {len(data)})")
    return not_found

